
public class JavaTypeCasting {

	public static void main(String[] args) {

		int x = 30;
		x += 6;
		int y = 47;
		y--;
		int sum = x + y;

		System.out.println(x);
		System.out.println(y);
		System.out.println(x + " + " + y + " = " + sum);
		System.out.println(x + " < " + y + " is " + (x < y));

	}
}
